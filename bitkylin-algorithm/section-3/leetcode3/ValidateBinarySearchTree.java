    class Solution {
        Integer min = null;

        public boolean isValidBST(TreeNode root) {
            return resolve(root);
        }

        private boolean resolve(TreeNode root) {
            if (root == null) {
                return true;
            }
            if (!resolve(root.left)) {
                return false;
            }
            if (min != null && min >= root.val) {
                return false;
            }
            min = root.val;
            return resolve(root.right);
        }
    }
